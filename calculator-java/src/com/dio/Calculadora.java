package com.dio;
public class Calculadora  {
	
	public static float multiplicar(float x, float y){
		return (x*y);
	}
	public static float subtrair( float x , float y){
		return x-y;
	}
	public static float somar( float x, float y){
		return x+y;
	}
	public static float dividir( float x, float y){
		return x/y;
	}
	
	
	
}
